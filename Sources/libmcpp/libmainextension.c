#include    "system.H"
#include    "internal.H"

void mcpp_help() {
    char * argv[] = {
        "mcpp",
        "-h",
        NULL
    };
    mcpp_lib_main(2, argv);
}
void mcpp_preprocessFile(const char * srcFile, const char * outFile) {
    char * argv[] = {
        "mcpp",
        "-NP",
        (char *)srcFile,
        (char *)outFile,
        NULL
    };
    mcpp_lib_main(4, argv);
}
